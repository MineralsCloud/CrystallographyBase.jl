        @test c2f([0.1838225731, 1.5127156619, 0.8216151148] / 2pi) ‚âà
            [-0.4461772527, 0.5538227473, 1 / 2]
        @test c2f ‚àò f2c == f2c ‚àò c2f == IdentityTransformation()
    end
