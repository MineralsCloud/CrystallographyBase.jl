        @test f2c([-1 / 2, 1 / 2, 1 / 2]) ‚âà
            [0, 1 / b, 1 / 2 / c] ‚âà
            [0, 1.5127156619, 0.8216151148] / 2pi  # T'
        @test f2c([0, 1 / 2, 0]) ‚âà
            [1 / 2 / a, 1 / 2 / b, 0] ‚âà
            [0.8538331021, 0.7563578310, 0] / 2pi  # S
        @test f2c([0, 1 / 2, 1 / 2]) ‚âà
            [1 / a, 1 / b, 1 / c] / 2 ‚âà
            [0.8538331021, 0.7563578310, 0.8216151148] / 2pi # R
